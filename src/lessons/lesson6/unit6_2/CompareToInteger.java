package lessons.lesson6.unit6_2;

public class CompareToInteger {
    public static void main(String[] args) {
        Integer a = 1;
        Integer b = 1;
        System.out.println(a.compareTo(b));

        a = 5;
        b = 2;
        System.out.println(a.compareTo(b));

        a = 2;
        b = 5;
        System.out.println(a.compareTo(b));

    }
}
